# int typecast
a = int(10)
print(f"value of a is {a} and data type of a is {type(a)}")
a = int(60.99)
print(f"value of a is {a} and data type of a is {type(a)}")
a = int("150")
print(f"value of a is {a} and data type of a is {type(a)}")

